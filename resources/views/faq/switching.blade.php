<h3>Switching or Renewing Vehicle</h3>
<div class="ui styled fluid accordion">
    <div class="title">
        <i class="dropdown icon"></i>
        How do I renew my current vehicle rental?
    </div>
    <div class="content">
        <p class="transition hidden">
            If you would like to renew your current vehicle at the same term as your current rental simply tap the ‘Renew’ button located on your current drive/vehicle profile page. If you would like to renew at a different term, please contact FleetHit customer service.
        </p>
    </div>
    <div class="title">
        <i class="dropdown icon"></i>
        What if I do not want to renew my current vehicle?
    </div>
    <div class="content">
        <p class="transition hidden">
            If you do not opt to renew your vehicle or swap for a new vehicle at the end of your rental period, FleetHit will contact you to schedule a time and location for vehicle drop off.
        </p>
    </div>
    <div class="title">
        <i class="dropdown icon"></i>
        Can I swap vehicles with available inventory in the FleetHit fleet?
    </div>
    <div class="content">
        <p class="transition hidden">
            Yes. You are eligible to request a swap with the FleetHit fleet at any time during your rental. Swaps will be granted on a first come first serve basis depending on availability. To request a swap, select an available vehicle shown in the app or contact FleetHit customer service.
        </p>
    </div>
    <div class="title">
        <i class="dropdown icon"></i>
        Can I swap vehicles with another FleetHit member?
    </div>
    <div class="content">
        <p class="transition hidden">
            Yes. You are eligible to request a swap with other FleetHit members any time during your rental. Your swap request will be sent to the other member and if the member accepts FleetHit will contact both of you to set up a time and location to swap vehicles.
        </p>
    </div>
    <div class="title">
        <i class="dropdown icon"></i>
        What do I need to do on the day of my vehicle pick up or drop off?
    </div>
    <div class="content">
        <p class="transition hidden">
            Each new vehicle rental will require you to complete a brief vehicle inspection and sign a new rental agreement. This is a 5-minute process that a FleetHit employee will meet you to complete.
        </p>
    </div>
    <div class="title">
        <i class="dropdown icon"></i>
        What is involved in a vehicle inspection?
    </div>
    <div class="content">
        <p class="transition hidden">
            During a vehicle inspection you get a chance to report the cleanliness of the vehicle and note any interior or exterior damage. Vehicle inspections help FleetHit keep vehicles in top shape for you and other members.
        </p>
    </div>
</div>