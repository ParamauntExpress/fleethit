<h3>Registration and Eligibility</h3>
<div class="ui styled fluid accordion">
    <div class="title">
        <i class="dropdown icon"></i>
        What are the eligibility requirements?
    </div>
    <div class="content">
        <p class="transition hidden">
            • 25 years of age or older<br/>
            • At least 3 years of recent driving history<br/>
            • Proof of auto insurance (personal or listed on another individuals’ policy) in the last 5 years<br/>
            • Valid in-state license and in-state residence<br/>
            • Valid credit card or debit card in your name<br/>
            • No more than 1 minor violation in the past year, no more than 2 minor violations in the past 3 years, and no major violations ever (DUI, reckless driving, speeding ticket >20mph over the speed limit)
        </p>
    </div>
    <div class="title">
        <i class="dropdown icon"></i>
        Is there any advance or down payment needed?
    </div>
    <div class="content">
        <p class="transition hidden">
            No. The only payment required is your weekly (or 28 day) rental fee and a $100 non-refundable membership application fee - upon approval.
        </p>
    </div>
    <div class="title">
        <i class="dropdown icon"></i>
        How long does registration take?
    </div>
    <div class="content">
        <p class="transition hidden">
            The registration process will take approximately 5 minutes and can be done from your mobile device.
            You will need your driver’s license and payment information to complete your registration.
            Once you have entered your information, it will take 1 to 2 days for FleetHit to validate your driver’s license and eligibility.
            When your account is validated you will receive an approval notification from FleetHit.
        </p>
    </div>
    <div class="title">
        <i class="dropdown icon"></i>
        Once my membership has been approved, what happens if my payment or driver’s license information changes?
    </div>
    <div class="content">
        <p class="transition hidden">
            You will need to keep your account information up to date.
            Use the My Account feature in the FleetHit mobile application to keep track of your information.
            To change your payment or driver’s license information, simply contact FleetHit customer service.
        </p>
    </div>
    <div class="title">
        <i class="dropdown icon"></i>
        I signed up for FleetHit but there are no vehicles available. How long will I have to wait?
    </div>
